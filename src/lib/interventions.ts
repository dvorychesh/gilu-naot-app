export type SchoolTrack = 'ELEMENTARY' | 'HIGH_SCHOOL'

const ELEMENTARY_BANK = `
## בנק התערבויות — יסודי

### א. תחום לימודי
- **הטרמה (Pre-teaching):** לימוד מילים קשות/מושגים לפני השיעור במליאה
- **פירוק משימות (Chunking):** חלוקת כל משימה ל-3 שלבים קצרים עם "וי" אחרי כל שלב
- **עזרים חזותיים:** שימוש בכרטיסיות ניווט או תמונות להמחשה
- **למידה פעילה:** שילוב תנועה או משחק בתוך תהליך הלמידה

### ב. תחום רגשי
- **"מחברת קשר":** יומן חיובי שעובר בין המורה לבית לציון הצלחות בלבד
- **תפקיד "עוזר המורה":** מתן אחריות פיזית (חלוקת דפים/מחיקת לוח) להעצמה
- **זמן איכות:** 5 דקות שיחה אישית בשבוע על נושאים שאינם לימודים
- **פינת רגיעה:** הגדרה מראש של מקום אליו ניתן ללכת כשמוצפים רגשית

### ג. תחום חברתי
- **הפסקות מובנות:** מתן משימה או משחק מוגדר להפסקה למניעת שוטטות
- **חונכות:** הצמדה לתלמיד בוגר או חבר כיתה חזק לפעילות משותפת
- **תיווך בזמן אמת:** המורה מלווה סיטואציה חברתית ועוזרת לפרש אותה נכון

### ד. תחום התנהגותי
- **טבלת עיצוב התנהגות:** חיזוקים ויזואליים (מדבקות/סמיילים) על התנהגות חיובית
- **סדר יום ויזואלי:** כרטיסייה על השולחן המראה מה סדר הפעולות היום
- **קישור להורים שבועי:** יומן תקשורת קצר בין מורה להורה
`

const HIGH_SCHOOL_BANK = `
## בנק התערבויות — על-יסודי

### א. תחום לימודי
- **אסטרטגיות ניהול זמן:** שימוש ביומן/אפליקציה לתכנון לוח מבחנים
- **מחוונים (Rubrics):** מתן קריטריונים ברורים להערכה לפני ביצוע המטלה
- **בחירה:** מתן אפשרות לבחור בין דרכי היבחנות שונות (עבודה/מבחן)
- **מיקוד:** בניית לו"ז פרטני להשלמת פערים לקראת בגרות

### ב. תחום רגשי
- **שיח רפלקטיבי:** שיחות אישיות המתמקדות ב"למה עשיתי את זה?" ו"מה יכולתי לעשות אחרת?"
- **הצבת יעדים:** בניית תוכנית יעדים אישית לטווח קצר וארוך עם התלמיד
- **אוטונומיה:** מתן אחריות על פרויקט כיתתי ללא התערבות המורה

### ג. תחום חברתי
- **מעורבות חברתית:** ניתוב התלמיד להתנדבות במקום שמעניין אותו
- **למידת עמיתים:** עבודה בקבוצות קטנות סביב פרויקט חקר
- **דילמות מוסריות:** שילוב התלמיד בדיונים ערכיים בכיתה

### ד. תחום התנהגותי
- **חוזה אישי (Contract):** הסכם כתוב וחתום בין המורה לתלמיד על "קווים אדומים"
- **שיחת בירור מכבדת:** פרוטוקול שיחה שמפריד בין המעשה לעושה
- **ניטור עצמי:** התלמיד ממלא דו"ח מעקב עצמי על נוכחות/התנהגות
- **הפנייה לייעוץ מקצועי:** תיאום עם יועצת בית הספר לתוכנית אישית
`

export function getInterventionBank(track: SchoolTrack): string {
  return track === 'ELEMENTARY' ? ELEMENTARY_BANK : HIGH_SCHOOL_BANK
}
